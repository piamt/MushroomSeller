import UIKit

extension UIColor {
    static func BrandBG() -> UIColor {
        return UIColor(red: 191.0 / 255.0, green: 208.0 / 255.0, blue: 208.0 / 255.0, alpha: 1.0)
    }
    
    static func MetalicSilver() -> UIColor {
        return UIColor(red: 141.0 / 255.0, green: 144.0 / 255.0, blue: 146.0 / 255.0, alpha: 1.0)
    }
}
